namespace AltThree\Tests\Bus;

use AltThree\Bus\Dispatcher;
use AltThree\Bus\BusServiceProvider;
use GrahamCampbell\TestBench\AbstractPackageTestCase;
use GrahamCampbell\TestBenchCore\ServiceProviderTrait;

/**
 * This is the service provider test class.
 *
 * @author Graham Campbell <graham@alt-three.com>
 */
class ServiceProviderTest extends AbstractPackageTestCase
{
    use ServiceProviderTrait;

    /**
     * Get the service provider class.
     *
     * @param \Illuminate\Contracts\Foundation\Application $app
     *
     * @return string
     */
    protected function getServiceProviderClass($app)
    {
        return BusServiceProvider::class;
    }

    public function testLockerIsInjectable()
    {
        $this->assertIsInjectable(Dispatcher::class);
    }
}
